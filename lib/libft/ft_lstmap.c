#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_list;
	t_list	*current;
	t_list	*new;

	new_list = NULL;
	current = lst;
	while (current)
	{
		new = ft_lstnew(f(current->content));
		if (!new)
		{
			ft_lstclear(&new_list, del);
			return (NULL);
		}
		ft_lstadd_back(&new_list, new);
		current = current->next;
	}
	return (new_list);
}
